package ru.vorobyov.socialmediaapi.constant;

/**
 * The enum of request statuses to friends.
 */
public enum FriendshipStatus {
    /**
     * Pending friendship status.
     */
    PENDING,
    /**
     * Accepted friendship status.
     */
    ACCEPTED,
    /**
     * Declined friendship status.
     */
    DECLINED
}

